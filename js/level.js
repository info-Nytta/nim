function szint_general(szint) {
	// a haladó szintet ketté kellene még osztani

	let kupacok=new Array();
	let kdb=0;
	let k=0;

	switch (szint) {
		case 11: // kezdő
			kdb=rand(3,4);
			for (let i=0; i<kdb; i++)
				if (i==2)  { 
					do
						k=rand(1,7);	
					while (!(kupacok.includes(k)));
					kupacok[i]=k;
				}
				else
				kupacok[i]=rand(1,7);
			break;
		case 12: // haladó
			kdb=3;
			for (let i=0; i<kdb; i++) { 
				do
					k=rand(1,10);
				while (kupacok.includes(k));
				kupacok[i]=k;
			}
			break;
		case 13: // profi
			kdb=rand(4,5);
			for (let i=0; i<kdb; i++) { 
				do
					k=rand(4,10);
				while (kupacok.includes(k));
				kupacok[i]=k;
			}
			break;
		default: // egyelőre teszteléshez
			kdb=2;
			for (let i=0; i<kdb; i++)
				kupacok[i]=rand(1,10);
			break;
	}
	return kupacok;
}


function szintszoveg(sz) {
	let szint="";
	if (sz<=10) szint="Tanuló"
	else if (sz==11) szint="Kezdő";
	else if (sz==12) szint="Haladó";
	else if (sz==13) szint="Profi";
	else szint=sz;
	return szint;
}
